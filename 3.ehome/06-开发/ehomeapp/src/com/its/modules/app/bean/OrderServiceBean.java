package com.its.modules.app.bean;

import java.util.List;

import com.its.modules.app.entity.BusinessInfo;
import com.its.modules.app.entity.OrderService;
import com.its.modules.app.entity.OrderServiceList;
import com.its.modules.app.entity.OrderTrack;

public class OrderServiceBean extends OrderService {
	private static final long serialVersionUID = 8878602382686018352L;
	// 商家信息
	private BusinessInfo businessInfo;
	// 订单清单
	private List<OrderServiceList> orderServiceLists;
	// 订单跟踪
	private List<OrderTrack> orderTracks;

	public BusinessInfo getBusinessInfo() {
		return businessInfo;
	}

	public void setBusinessInfo(BusinessInfo businessInfo) {
		this.businessInfo = businessInfo;
	}

	public List<OrderServiceList> getOrderServiceLists() {
		return orderServiceLists;
	}

	public void setOrderServiceLists(List<OrderServiceList> orderServiceLists) {
		this.orderServiceLists = orderServiceLists;
	}

	public List<OrderTrack> getOrderTracks() {
		return orderTracks;
	}

	public void setOrderTracks(List<OrderTrack> orderTracks) {
		this.orderTracks = orderTracks;
	}
}