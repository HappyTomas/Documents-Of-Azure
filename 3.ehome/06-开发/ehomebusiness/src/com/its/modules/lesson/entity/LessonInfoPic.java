package com.its.modules.lesson.entity;

/**
 * 课程图片信息Entity
 * @author ChenXiangyu
 * @version 2017-08-02
 */
public class LessonInfoPic {
	private String imgBase64;		//BASE64编码格式的图片
	private String type;		// 图片格式
	private String imgUrl;       //BASE64编码格式的图片
	private String myfileid;        // 图片格式
	
	public String getImgBase64() {
		return imgBase64;
	}
	public void setImgBase64(String imgBase64) {
		this.imgBase64 = imgBase64;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getImgUrl() {
		return imgUrl;
	}
	public void setImgUrl(String imgUrl) {
		this.imgUrl = imgUrl;
	}
	public String getMyfileid() {
		return myfileid;
	}
	public void setMyfileid(String myfileid) {
		this.myfileid = myfileid;
	}
}
