package com.its.modules.rabbitmq.entity;

import com.its.common.persistence.DataEntity;

public class ProductFlow extends DataEntity<ProductFlow> {

	private static final long serialVersionUID = 1L;

	private String memberId;// 会员ID
	private String phoneNumber;// 电话号
	private String createTime;// 时间
	private String account;// 金额
	private String productFlowNum;// 流水号
	private String productId;// 标的id

	public String getMemberId() {
		return memberId;
	}

	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}

	public String getPhoneNumber() {
		return phoneNumber;
	}

	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}

	public String getProductFlowNum() {
		return productFlowNum;
	}

	public void setProductFlowNum(String productFlowNum) {
		this.productFlowNum = productFlowNum;
	}

	public String getProductId() {
		return productId;
	}

	public void setProductId(String productId) {
		this.productId = productId;
	}

	@Override
	public String toString() {
		return "ProductFlow [memberId=" + memberId + ", phoneNumber="
				+ phoneNumber + ", createTime=" + createTime + ", account="
				+ account + ", productFlowNum=" + productFlowNum
				+ ", productId=" + productId + "]";
	}

}
