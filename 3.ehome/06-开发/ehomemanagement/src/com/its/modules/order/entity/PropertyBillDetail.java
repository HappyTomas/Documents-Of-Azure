package com.its.modules.order.entity;

import com.its.common.persistence.DataEntity;

public class PropertyBillDetail extends DataEntity<PropertyDeal> {
	
	private static final long serialVersionUID = 1L;
	private String id;// 主键
	private String propertyBillId;// 物业缴费单ID
	private String roomCertifyId;// 房间认证ID
	private String period;// 缴费周期
	private String feeType;// 费用类型：0-住宅物业管理费；1-水费（住宅）2:车位管理费
	private String money;// 金额
	private String remarks;// 备注

	public PropertyBillDetail() {
		super();
	}

	public PropertyBillDetail(String id) {
		super(id);
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPropertyBillId() {
		return propertyBillId;
	}

	public void setPropertyBillId(String propertyBillId) {
		this.propertyBillId = propertyBillId;
	}

	public String getRoomCertifyId() {
		return roomCertifyId;
	}

	public void setRoomCertifyId(String roomCertifyId) {
		this.roomCertifyId = roomCertifyId;
	}

	public String getPeriod() {
		return period;
	}

	public void setPeriod(String period) {
		this.period = period;
	}

	public String getFeeType() {
		return feeType;
	}

	public void setFeeType(String feeType) {
		this.feeType = feeType;
	}

	public String getMoney() {
		return money;
	}

	public void setMoney(String money) {
		this.money = money;
	}

	public String getRemarks() {
		return remarks;
	}

	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

}
